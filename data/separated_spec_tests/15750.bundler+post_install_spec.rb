# frozen_string_literal: true

RSpec.describe "bundle install" do
  context "with gem sources" do
    context "when gems include post install messages" do
      it "should display the post-install messages after installing" 

    end

    context "when gems do not include post install messages" do
      it "should not display any post-install messages" 

    end

    context "when a dependecy includes a post install message" do
      it "should display the post install message" 

    end
  end

  context "with git sources" do
    context "when gems include post install messages" do
      it "should display the post-install messages after installing" 


      it "should display the post-install messages if repo is updated" 


      it "should not display the post-install messages if repo is not updated" 

    end

    context "when gems do not include post install messages" do
      it "should not display any post-install messages" 

    end
  end

  context "when ignore post-install messages for gem is set" do
    it "doesn't display any post-install messages" 

  end

  context "when ignore post-install messages for all gems" do
    it "doesn't display any post-install messages" 

  end
end

